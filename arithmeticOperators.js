// Arithmetic Operators

//Variables
let num1 = 10;
let num2 = 5;

//Addition
console.log(num1 + num2);

//Subtraction
console.log(num1 - num2);

//Multiplication
console.log(num1 * num2);

//Division
console.log(num1 / num2);

//Squared
console.log(num1 ** num2);

//Modulo
console.log(num1 % num2);
