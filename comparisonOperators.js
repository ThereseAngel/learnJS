// Comparison Operators
// > < >= <== === !=

// Variable
let price = 200;

// Greater than
console.log(price > 300); //false

// Less than
console.log(price < 200); //false

// Greater than equal
console.log(price >= 100); //true

// Less than equal
console.log(price <= 500); //true

// Equal
console.log(price === 200); //true

// Not Equal
console.log(price !== 300); //true
